inline float Evaluate(FireStarterData data, float n)
{
    n = data.d[24] *= n;
    n = data.d[25] += n;
    n = data.d[17] *= n;
    n = data.d[31] *= n;
    n = data.d[17] *= n;
    n = data.d[14] += n;
    n += data.d[12];
    n *= data.d[27];
    n = data.d[9] += n;
    n = data.d[11] += n;
    n = data.d[18] += n;
    n += data.d[7];
    n += data.d[17];
    n = data.d[18] += n;
    n *= data.d[10];
    n = data.d[0] *= n;
    n += data.d[9];
    n += data.d[18];
    n *= data.d[0];
    n *= data.d[11];
    n = data.d[19] *= n;
    n *= data.d[14];
    n *= data.d[19];
    n = data.d[31] += n;
    n += data.d[31];
    n *= data.d[26];
    n *= data.d[23];
    n = data.d[3] += n;
    n += data.d[24];
    n += data.d[3];
    n += data.d[28];
    n *= data.d[25];
    return isfinite(n) ? n : 0.0f;
} // Evaluate
